public class P1 {
    public static void main(String[] args) {
        
        // 조건문
    {
        System.out.println("코드1");
        System.out.println("코드2");
        System.out.println("코드3");
        System.out.println("코드4");
        
        int value = 30;
        
        if(value > 10) {   // 자바에서 if() 안에는 boolean 타입, 즉 true or false만 가능하다.
            System.out.println(value + "value가 10보다 클 때 나오는 로직");
        } else {
            System.out.println(value + "value가 10보다 작을 때 나오는 로직");
        }
        

        System.out.println("프로젝트 종료");
    }
    }

}
